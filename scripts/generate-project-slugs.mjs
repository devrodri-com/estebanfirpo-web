import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const ts = require("typescript");
const repositoryRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const dataRoot = path.join(repositoryRoot, "src/data");
const catalogEntry = path.join(dataRoot, "projects/index.ts");
const outputFile = path.join(dataRoot, "projects/public-slugs.generated.ts");
const temporaryOutput = fs.mkdtempSync(path.join(os.tmpdir(), "estebanfirpo-project-slugs-"));

const formatHost = {
  getCanonicalFileName: (fileName) => fileName,
  getCurrentDirectory: () => repositoryRoot,
  getNewLine: () => "\n",
};

try {
  const program = ts.createProgram([catalogEntry], {
    target: ts.ScriptTarget.ES2020,
    module: ts.ModuleKind.CommonJS,
    moduleResolution: ts.ModuleResolutionKind.Node10,
    esModuleInterop: true,
    strict: true,
    skipLibCheck: true,
    noEmitOnError: true,
    rootDir: dataRoot,
    outDir: temporaryOutput,
  });
  const emitResult = program.emit();
  const errors = [...ts.getPreEmitDiagnostics(program), ...emitResult.diagnostics].filter(
    (diagnostic) => diagnostic.category === ts.DiagnosticCategory.Error,
  );

  if (errors.length > 0) {
    throw new Error(ts.formatDiagnostics(errors, formatHost));
  }

  const compiledCatalog = require(path.join(temporaryOutput, "projects/index.js"));
  if (!Array.isArray(compiledCatalog.ALL_PROJECTS)) {
    throw new Error("The compiled catalog does not export ALL_PROJECTS.");
  }

  const slugs = compiledCatalog.ALL_PROJECTS.map((project) => project.slug);
  if (
    slugs.length === 0 ||
    slugs.some(
      (slug) =>
        typeof slug !== "string" ||
        !/^\/proyectos\/[a-z0-9]+(?:-[a-z0-9]+)*$/.test(slug),
    )
  ) {
    throw new Error("The public catalog contains an invalid project slug.");
  }

  const uniqueSlugs = [...new Set(slugs)].sort();
  if (uniqueSlugs.length !== slugs.length) {
    throw new Error("The public catalog contains duplicate project slugs.");
  }

  const generatedSource = `// Generated by scripts/generate-project-slugs.mjs. Do not edit manually.\n\nexport const PUBLIC_PROJECT_SLUGS = ${JSON.stringify(uniqueSlugs, null, 2)} as const;\n`;
  const currentSource = fs.existsSync(outputFile) ? fs.readFileSync(outputFile, "utf8") : "";

  if (currentSource !== generatedSource) {
    fs.writeFileSync(outputFile, generatedSource, "utf8");
  }

  console.log(`Project slug manifest: ${uniqueSlugs.length} public slugs.`);
} finally {
  fs.rmSync(temporaryOutput, { recursive: true, force: true });
}
